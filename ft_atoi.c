#include "libft.h"

int	ft_atoi(const char *s)
{
	int	i;
	int	j;
	int	sign;

	i = 0;
	j = 0;
	sign = 1;
	while (ft_iswhite(s[i] != 0))
		i++;
	if (s[i] == '-')
	{
		i++;
		sign = -1;
	}
	if (s[i] == '+')
		i++;
	while ((s[i] != '\0') && ((s[i] >= '0') && (s[i] <= '9')))
		j = ((j * 10) + (s[i++] - '0'));
	return (j * sign);
}
